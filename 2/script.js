// Используем GSAP для анимации при наведении
gsap.from(".btn", {
    duration: 0.6,
    scale: 0.8,
    opacity: 0,
    stagger: 0.1,  // Задержка между анимациями кнопок
    ease: "back.out(1.7)"
});

// Пример анимации для кнопки с более сложным эффектом
gsap.to(".btn1", {
    duration: 1,
    x: 100,  // Сдвиг кнопки по оси X
    rotation: 360,
    repeat: -1,  // Бесконечное повторение
    yoyo: true,  // Плавное возврашение в исходное положение
    ease: "sine.inOut"
});
